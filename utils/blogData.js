// Utility Functions for Blog Management

/**
 * Get all published blog posts
 * @returns {Array} Array of published blog posts
 */
export const getAllBlogPosts = () => {
  return blogPosts.filter((post) => post.published);
};

/**
 * Get blog posts sorted by date (newest first)
 * @param {number} limit - Optional limit for number of posts
 * @returns {Array} Array of blog posts sorted by date
 */
export const getBlogPostsByDate = (limit = null) => {
  const posts = getAllBlogPosts().sort(
    (a, b) => new Date(b.date) - new Date(a.date)
  );
  return limit ? posts.slice(0, limit) : posts;
};

/**
 * Get latest blog posts (for homepage component)
 * @param {number} count - Number of latest posts to return (default: 3)
 * @returns {Array} Array of latest blog posts
 */
export const getLatestBlogPosts = (count = 3) => {
  return getBlogPostsByDate(count);
};

/**
 * Get featured blog posts
 * @returns {Array} Array of featured blog posts
 */
export const getFeaturedBlogPosts = () => {
  return getAllBlogPosts().filter((post) => post.featured);
};

/**
 * Get blog post by ID
 * @param {number} id - Blog post ID
 * @returns {Object|null} Blog post object or null if not found
 */
export const getBlogPostById = (id) => {
  return getAllBlogPosts().find((post) => post.id === parseInt(id)) || null;
};

/**
 * Get blog post by slug
 * @param {string} slug - Blog post slug
 * @returns {Object|null} Blog post object or null if not found
 */
export const getBlogPostBySlug = (slug) => {
  return getAllBlogPosts().find((post) => post.slug === slug) || null;
};

/**
 * Get blog posts by category
 * @param {string} category - Category name
 * @returns {Array} Array of blog posts in the category
 */
export const getBlogPostsByCategory = (category) => {
  return getAllBlogPosts().filter((post) => post.category === category);
};

/**
 * Get all unique categories
 * @returns {Array} Array of unique category names
 */
export const getAllCategories = () => {
  const categories = getAllBlogPosts().map((post) => post.category);
  return [...new Set(categories)];
};

/**
 * Get all unique tags
 * @returns {Array} Array of unique tags
 */
export const getAllTags = () => {
  const tags = getAllBlogPosts().flatMap((post) => post.tags);
  return [...new Set(tags)];
};

/**
 * Search blog posts by title, excerpt, or content
 * @param {string} query - Search query
 * @returns {Array} Array of matching blog posts
 */
export const searchBlogPosts = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return getAllBlogPosts().filter(
    (post) =>
      post.title.toLowerCase().includes(lowercaseQuery) ||
      post.excerpt.toLowerCase().includes(lowercaseQuery) ||
      post.content.toLowerCase().includes(lowercaseQuery) ||
      post.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery))
  );
};

/**
 * Get related blog posts (by category or tags)
 * @param {Object} currentPost - Current blog post object
 * @param {number} limit - Number of related posts to return (default: 3)
 * @returns {Array} Array of related blog posts
 */
export const getRelatedBlogPosts = (currentPost, limit = 3) => {
  const allPosts = getAllBlogPosts().filter(
    (post) => post.id !== currentPost.id
  );

  // Find posts with same category first
  const sameCategory = allPosts.filter(
    (post) => post.category === currentPost.category
  );

  // Find posts with similar tags
  const similarTags = allPosts.filter((post) =>
    post.tags.some((tag) => currentPost.tags.includes(tag))
  );

  // Combine and remove duplicates
  const related = [...sameCategory, ...similarTags];
  const uniqueRelated = related.filter(
    (post, index, self) => index === self.findIndex((p) => p.id === post.id)
  );

  return uniqueRelated.slice(0, limit);
};

/**
 * Format date for display
 * @param {string} dateString - Date string in YYYY-MM-DD format
 * @returns {string} Formatted date string
 */
export const formatBlogDate = (dateString) => {
  const date = new Date(dateString);
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
  });
};

/**
 * Format view count for display
 * @param {number} views - Number of views
 * @returns {string} Formatted view count
 */
export const formatViewCount = (views) => {
  if (views >= 1000) {
    return `${(views / 1000).toFixed(1)}K`;
  }
  return views.toString();
};
